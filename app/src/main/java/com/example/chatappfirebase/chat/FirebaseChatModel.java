package com.example.chatappfirebase.chat;

public class FirebaseChatModel {

    String name;
    String image;
    String uid;
    String status;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }


    public String getUid() {
        return uid;
    }

    public String getStatus() {
        return status;
    }

}
